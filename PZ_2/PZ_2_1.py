#Дано расстояние L в сантиметрах.
#Используя операцию деления нацело, найти количество полных метров в нем (1 метр =100 см).

a = input("Введите расстояние: ")
while type(a) != float:     #Обработка исключений
    try:
        a = float(a)
    except ValueError:
        print("Неправильно ввели")
        a = input("Введите расстояние: ")
m = a // 100
print(f"Количество полных метров: {m}")